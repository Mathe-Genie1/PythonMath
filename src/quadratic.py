def roots(a, b, c):
    discriminant = b**2 - 4 * a * c
    if discriminant > 0:
        root1 = (-b + discriminant**0.5) / (2 * a)
        root2 = (-b - discriminant**0.5) / (2 * a)
        return root1, root2
    elif discriminant == 0:
        root = -b / (2 * a)
        return root
    else:
        return None

print(roots(1,1,1))
print(roots(1,-8,16))
print(roots(1,-5,6))